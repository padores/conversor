package test;
public class Conversor {

	public int celsiusParaFahrenheit() {
		return (9 * 40) / 5 + 32;
	}

	public int celsiusParaFahrenheit(int valorCelcius) {
		return (9 * valorCelcius) / 5 + 32;

	}

	public double celsiusParaFahrenheit(double valorCelcius) {
		return (9 * valorCelcius) / 5 + 32;
	}

}
